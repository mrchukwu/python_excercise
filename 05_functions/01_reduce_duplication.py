# A simple function that takes orders from people

def print_order(name, chai_type):
  print(f"{name} ordered {chai_type} chai")

# calling the function multiple times with different arguments
print_order("Usman", "Masala")
print_order("Ali", "Ginger")
print_order("Ahmed", "Elaichi")
print_order("Bilal", "Lemon")
print_order("Fahad", "Green")